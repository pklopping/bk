		Bryan Klopping is an American multimedia artist born in Omaha, Nebraska in 1986. <br><br>

		In 2004, he enrolled at the University of Nebraska Lincoln for fine arts with an emphasis in sculpture and digital performance art. This included study in Kyoto and Tokyo, Japan. Between the years of 2005 and 2012 he created and toured with bands internationally. From 2010-2011 He co-curated house show venue, Clawfoot House, in Lincoln Nebraska, which brought in alternative music from all over the world. Klopping then became the intern for Nebraska based art gallery, Tugboat Gallery, while completing his residency at the Parrish Project. 

		<br><br>In 2012, he moved to New York City and soon joined the lower Manhattan artist group, Glassine Box, as an intern. Soon after, Klopping began collaborating with artist Domingo Zapata. Klopping's work leans towards geometric abstract expressionism as he attempts to understand human and physical relationships between space and time, as many of his works reference maps. 

		<br><br>He is currently working on large scale interpretations of these maps to be presented as multimedia pieces on stretched canvas
